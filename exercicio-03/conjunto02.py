# Remover elementos duplicados de uma lista com a utilização de conjuntos:

lista_com_duplicados = [1, 2, 4, 2, 1, 3, 2, 5, 3, 5]
conjunto_sem_duplicados = set(lista_com_duplicados)

print(f"Lista com Elementos Duplicados: {lista_com_duplicados}")

print(f"Lista sem Elementos Duplicados: {conjunto_sem_duplicados}")
